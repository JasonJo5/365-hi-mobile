function setLanguage(lang) {
    const dict = translations[lang] || translations.en;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key] === undefined) return;
        if (el.getAttribute('data-i18n-html') === 'true') {
            el.innerHTML = dict[key];
        } else {
            el.textContent = dict[key];
        }
    });
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
        const active = btn.getAttribute('data-lang-btn') === lang;
        btn.classList.toggle('text-primary', active);
        btn.classList.toggle('bg-primary/15', active);
    });
    document.documentElement.setAttribute('lang', lang);
    try { localStorage.setItem('site_lang', lang); } catch (e) {}
}

document.addEventListener('DOMContentLoaded', function () {
    let saved = 'en';
    try { saved = localStorage.getItem('site_lang') || 'en'; } catch (e) {}
    setLanguage(saved);
});

// ===== Light / Dark theme =====
function applyTheme(theme) {
    const root = document.documentElement;
    const icon = document.getElementById('theme-toggle-icon');
    if (theme === 'light') {
        root.classList.add('light');
    } else {
        root.classList.remove('light');
    }
    if (icon) {
        icon.textContent = theme === 'light' ? 'light_mode' : 'dark_mode';
    }
    try { localStorage.setItem('site_theme', theme); } catch (e) {}
}

function toggleTheme() {
    const isLight = document.documentElement.classList.contains('light');
    applyTheme(isLight ? 'dark' : 'light');
}

(function initTheme() {
    let saved = null;
    try { saved = localStorage.getItem('site_theme'); } catch (e) {}
    if (!saved) {
        saved = (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches) ? 'light' : 'dark';
    }
    // Apply immediately (before DOMContentLoaded) to avoid a flash of the wrong theme.
    if (saved === 'light') {
        document.documentElement.classList.add('light');
    }
    document.addEventListener('DOMContentLoaded', function () {
        const icon = document.getElementById('theme-toggle-icon');
        if (icon) icon.textContent = saved === 'light' ? 'light_mode' : 'dark_mode';
    });
})();

